package semana_2;

public class Ejercicio_6 {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++){
            System.out.println(i);
        }
        int n = 2;
        while (n <= 20){
            System.out.println(n);
            n += 2;
        }
        int j = 10;
        do {
            System.out.println(j);
            j--;
        } while (j >= 1);
    }
}

